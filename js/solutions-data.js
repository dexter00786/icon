function getSolutionData(solutionId) {
  return solutionsData[solutionId] || null;
}

function generateSolutionPage(solutionId) {
  const solution = getSolutionData(solutionId);
  if (!solution) {
    console.error('Solution not found:', solutionId);
    return;
  }

  // Update page title
  document.title = `${solution.title} | Icon Power Solutions`;

  // Update hero section
  if (solution.hero) {
    const heroBackground = document.querySelector('.hero-background');
    const heroTitle = document.querySelector('.hero-title');
    const heroSubtitle = document.querySelector('.hero-subtitle');
    const heroDescription = document.querySelector('.hero-description');
    const heroCtaBtn = document.querySelector('.hero-cta-btn');

    if (heroBackground) {
      heroBackground.style.backgroundImage = `url('${solution.hero.backgroundImage}')`;
    }
    if (heroTitle) {
      heroTitle.textContent = solution.hero.title;
    }
    if (heroSubtitle) {
      heroSubtitle.textContent = solution.hero.subtitle;
    }
    if (heroDescription) {
      heroDescription.textContent = solution.hero.description;
    }
    if (heroCtaBtn) {
      heroCtaBtn.textContent = solution.hero.ctaText;
      heroCtaBtn.href = solution.hero.ctaLink;
    }
  }

  // Update main heading
  const mainHeading = document.querySelector('h1');
  if (mainHeading) {
    mainHeading.textContent = solution.title;
  }

  // Update solution intro
  const introSubtitle = document.querySelector('.solution-intro h2');
  const introDescription = document.querySelector('.solution-intro p');
  if (introSubtitle) {
    introSubtitle.textContent = solution.subtitle;
  }
  if (introDescription) {
    introDescription.textContent = solution.description;
  }

  // Update features
  const featuresGrid = document.querySelector('.features-grid');
  if (featuresGrid) {
    featuresGrid.innerHTML = '';
    solution.features.forEach(feature => {
      const featureCard = document.createElement('div');
      featureCard.className = 'feature-card';
      featureCard.innerHTML = `
        <h4>${feature.title}</h4>
        <p>${feature.description}</p>
      `;
      featuresGrid.appendChild(featureCard);
    });
  }

  // Update benefits
  const benefitsList = document.querySelector('.solution-benefits ul');
  if (benefitsList) {
    benefitsList.innerHTML = '';
    solution.benefits.forEach(benefit => {
      const benefitItem = document.createElement('li');
      benefitItem.textContent = benefit;
      benefitsList.appendChild(benefitItem);
    });
  }

  // Update applications
  const applicationsList = document.querySelector('.solution-applications ul');
  if (applicationsList) {
    applicationsList.innerHTML = '';
    solution.applications.forEach(application => {
      const applicationItem = document.createElement('li');
      applicationItem.textContent = application;
      applicationsList.appendChild(applicationItem);
    });
  }
}

// Initialize page when DOM is loaded
document.addEventListener('DOMContentLoaded', function() {
  // Get solution ID from URL parameter or filename
  const urlParams = new URLSearchParams(window.location.search);
  const solutionId = urlParams.get('solution') || getSolutionIdFromPath();

  if (solutionId) {
    generateSolutionPage(solutionId);
  }
});

function getSolutionIdFromPath() {
  const path = window.location.pathname;
  const filename = path.split('/').pop();
  return filename.replace('.html', '');
}
